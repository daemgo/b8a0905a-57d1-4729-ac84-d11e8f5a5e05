// 获取状态标签
export const getStatusLabel = (status: string, options: typeof projectStatusOptions) => {
  return options.find(opt => opt.value === status)?.label || status;
};

// 获取状态颜色
export const getStatusColor = (status: string, options: typeof projectStatusOptions) => {
  const colorMap: Record<string, string> = {
    gray: "bg-gray-100 text-gray-800 dark:bg-gray-800 dark:text-gray-300",
    blue: "bg-blue-100 text-blue-800 dark:bg-blue-900 dark:text-blue-300",
    yellow: "bg-yellow-100 text-yellow-800 dark:bg-yellow-900 dark:text-yellow-300",
    purple: "bg-purple-100 text-purple-800 dark:bg-purple-900 dark:text-purple-300",
    green: "bg-green-100 text-green-800 dark:bg-green-900 dark:text-green-300",
    red: "bg-red-100 text-red-800 dark:bg-red-900 dark:text-red-300",
  };
  const option = options.find(opt => opt.value === status);
  return option ? colorMap[option.color] || colorMap.gray : colorMap.gray;
};
